
public class Variables {

	public static void main(String[] args) {
		int edad;
		
		edad=35;   // Declaración e iniciación de variable en una única línea
		           // Esto se usa para hacer anotaciones o dejar comentarios que splo ocupen una línea
		/* esto se usa cuando el texto o anotación personal
		 * es para más de una línea 
		 */
		System.out.println(edad);
		
		edad=75;
		
		System.out.println(edad);

	}

}
